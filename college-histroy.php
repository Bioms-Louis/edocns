            <p>The State school of Nursing, Benin (as the school was formally known) was established in 1954 by the then western region Government to train Nurses who will satisfy the medical and health needs of the people.</p>
            
            <br>
            
            <p>In 1968, the school was approved to run a midwifery program. Upon that, the name was re-coined to “State School of Nursing and Midwifery” to capture the latter. This professional institution became a renowned education and health hub in Nigeria. Having experienced robust development, the School moved to its current location in 1977 and the Foundation stone laid on Monday 9th May, 1977 by His Excellency, Commodore Husaini Abdullahi P.S.C. the Military Governor of the then Bendel State of Nigeria.</p>
            
            <br>
            
            <p>However, in 2012, the school lost its accreditation due to poor funding, poor record keeping system, inadequate teaching and non-teaching staffs, lack of enabling law(s) backing its existence, inadequate offices, among others.</p>
            
            <br>
            
            <p>Upon coming on board, the Edo State Governor, His Excellency Mr. Godwin Obaseki swung into action with his “Making Edo Great Again (MEGA) Agenda” — to make Edo an Education and Health Hub in the World, to improve the health-care system of Edo people and to produce well-trained professional Nurses for Local and International consumptions.</p>
            
            <br>
            
            <p>Sequel to the above, a full assessment of the School was ordered and recommendations made, followed by proper planning, implementations, and renovations, resulting to what we have today.
                Subsequent upon the renovations of the existing structures and the acquisitions of new medical, teaching and demonstration equipment, the Edo State House of Assembly on 12th October this year, passed the “Edo Sate College of Nursing Sciences Law of 2021” while repealing the “Edo State College of Nursing and Midwifery Law of 2015”. And on 25th of October 2021, the School received a two -year provincial accreditation from the Nursing and Midwifery Council of Nigeria (NMCN) to re-open the Nursing and Midwifery Programs, so we are fully back on our feet. But this time, stronger!</p>
            
            
            <br>

            <p>It is imperative to note here that the vision of the Edo State Government is to have a College of Nursing that will produce well trained and competent Nurses who will compete favorably with their international counterparts. The school is not meant for Local consumptions only but also for the international community as we are not affiliated with any university yet but seeking to affiliate or collaborate with universities abroad and locally.</p>
